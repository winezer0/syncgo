// md5x16_gather_amd64.s — AVX512 VPGATHERDD load+transpose (16-way).
// K mask must be reloaded before each gather (VPGATHERDD zeros mask).

#include "textflag.h"

// func md5x16LoadTransposeGather(data []byte, offsets *[16]int, chunk int, x *[16][16]uint32)
TEXT ·md5x16LoadTransposeGather(SB), NOSPLIT, $128-48
	MOVQ data+0(FP), R8
	MOVQ offsets+24(FP), R9
	MOVQ chunk+32(FP), R10
	MOVQ x+40(FP), R11

	MOVQ R10, CX
	SHLQ $6, CX

	// Build 16 int32 indices on stack
	MOVQ 0(R9), R12; ADDQ CX, R12; MOVL R12, (SP)
	MOVQ 8(R9), R13; ADDQ CX, R13; MOVL R13, 4(SP)
	MOVQ 16(R9), R14; ADDQ CX, R14; MOVL R14, 8(SP)
	MOVQ 24(R9), R15; ADDQ CX, R15; MOVL R15, 12(SP)
	MOVQ 32(R9), AX;  ADDQ CX, AX;  MOVL AX, 16(SP)
	MOVQ 40(R9), BX;  ADDQ CX, BX;  MOVL BX, 20(SP)
	MOVQ 48(R9), DX;  ADDQ CX, DX;  MOVL DX, 24(SP)
	MOVQ 56(R9), DI;  ADDQ CX, DI;  MOVL DI, 28(SP)
	MOVQ 64(R9), R12; ADDQ CX, R12; MOVL R12, 32(SP)
	MOVQ 72(R9), R13; ADDQ CX, R13; MOVL R13, 36(SP)
	MOVQ 80(R9), R14; ADDQ CX, R14; MOVL R14, 40(SP)
	MOVQ 88(R9), R15; ADDQ CX, R15; MOVL R15, 44(SP)
	MOVQ 96(R9), AX;  ADDQ CX, AX;  MOVL AX, 48(SP)
	MOVQ 104(R9), BX; ADDQ CX, BX;  MOVL BX, 52(SP)
	MOVQ 112(R9), DX; ADDQ CX, DX;  MOVL DX, 56(SP)
	MOVQ 120(R9), DI; ADDQ CX, DI;  MOVL DI, 60(SP)

	LEAQ (SP), R12
	VMOVDQU32 (R12), Z0

	// Pre-load word offset constants
	VMOVDQU32 WORD16_4<>(SB), Z2
	VMOVDQU32 WORD16_8<>(SB), Z3
	VMOVDQU32 WORD16_12<>(SB), Z4
	VMOVDQU32 WORD16_16<>(SB), Z5
	VMOVDQU32 WORD16_20<>(SB), Z6
	VMOVDQU32 WORD16_24<>(SB), Z7

	// Word 0: reload K1 mask, then gather
	KXNORW K1, K1, K1; VPGATHERDD (R8)(Z0*1), K1, Z10; VMOVDQU32 Z10, 0(R11)
	// Words 1-5
	KXNORW K1, K1, K1; VPADDD Z0, Z2, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 64(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z3, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 128(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z4, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 192(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z5, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 256(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z6, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 320(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z7, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 384(R11)

	// Reload constants for words 6-11
	VMOVDQU32 WORD16_28<>(SB), Z2
	VMOVDQU32 WORD16_32<>(SB), Z3
	VMOVDQU32 WORD16_36<>(SB), Z4
	VMOVDQU32 WORD16_40<>(SB), Z5
	VMOVDQU32 WORD16_44<>(SB), Z6
	VMOVDQU32 WORD16_48<>(SB), Z7

	KXNORW K1, K1, K1; VPADDD Z0, Z2, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 448(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z3, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 512(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z4, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 576(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z5, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 640(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z6, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 704(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z7, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 768(R11)

	// Reload for words 12-14
	VMOVDQU32 WORD16_52<>(SB), Z2
	VMOVDQU32 WORD16_56<>(SB), Z3
	VMOVDQU32 WORD16_60<>(SB), Z4

	KXNORW K1, K1, K1; VPADDD Z0, Z2, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 832(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z3, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 896(R11)
	KXNORW K1, K1, K1; VPADDD Z0, Z4, Z20; VPGATHERDD (R8)(Z20*1), K1, Z10; VMOVDQU32 Z10, 960(R11)

	VZEROUPPER
	RET

