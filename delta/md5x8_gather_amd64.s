// md5x8_gather_amd64.s — VPGATHERDD gather load + transpose (8-way AVX2).
// NOTE: VPGATHERDD zeros the mask after execution — must reload before each gather.

#include "textflag.h"

// func md5x8LoadTransposeGather(data []byte, offsets *[8]int, chunk int, x *[16][8]uint32)
TEXT ·md5x8LoadTransposeGather(SB), NOSPLIT, $0-48
	MOVQ data+0(FP), R8
	MOVQ offsets+24(FP), R9
	MOVQ chunk+32(FP), R10
	MOVQ x+40(FP), R11

	MOVQ R10, CX; SHLQ $6, CX

	// Build Y7 = byte offsets (no SHRQ — use scale=1 for byte-level addressing)
	MOVQ (R9), R12; ADDQ CX, R12; VMOVD R12, X0
	MOVQ 8(R9), R13; ADDQ CX, R13; VPINSRD $1, R13, X0, X0
	MOVQ 16(R9), R14; ADDQ CX, R14; VPINSRD $2, R14, X0, X0
	MOVQ 24(R9), R15; ADDQ CX, R15; VPINSRD $3, R15, X0, X0
	MOVQ 32(R9), AX;  ADDQ CX, AX;  VMOVD AX, X1
	MOVQ 40(R9), BX;  ADDQ CX, BX;  VPINSRD $1, BX, X1, X1
	MOVQ 48(R9), DX;  ADDQ CX, DX;  VPINSRD $2, DX, X1, X1
	MOVQ 56(R9), DI;  ADDQ CX, DI;  VPINSRD $3, DI, X1, X1
	VINSERTI128 $1, X1, Y0, Y0
	VMOVDQU Y0, Y7

	// Y3 = all-ones mask (pre-compute once, VPGATHERDD zeros the mask each time)
	VPCMPEQD Y3, Y3, Y3

	// Word 0: reload mask to Y2, then gather (VPGATHERDD zeros mask each time)
	VMOVDQA Y3, Y2
	VPGATHERDD Y2, (R8)(Y7*1), Y1
	VMOVDQU Y1, 0(R11)

	VMOVDQA Y3, Y2; VPGATHERDD Y2, 4(R8)(Y7*1), Y1; VMOVDQU Y1, 32(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 8(R8)(Y7*1), Y1; VMOVDQU Y1, 64(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 12(R8)(Y7*1), Y1; VMOVDQU Y1, 96(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 16(R8)(Y7*1), Y1; VMOVDQU Y1, 128(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 20(R8)(Y7*1), Y1; VMOVDQU Y1, 160(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 24(R8)(Y7*1), Y1; VMOVDQU Y1, 192(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 28(R8)(Y7*1), Y1; VMOVDQU Y1, 224(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 32(R8)(Y7*1), Y1; VMOVDQU Y1, 256(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 36(R8)(Y7*1), Y1; VMOVDQU Y1, 288(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 40(R8)(Y7*1), Y1; VMOVDQU Y1, 320(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 44(R8)(Y7*1), Y1; VMOVDQU Y1, 352(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 48(R8)(Y7*1), Y1; VMOVDQU Y1, 384(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 52(R8)(Y7*1), Y1; VMOVDQU Y1, 416(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 56(R8)(Y7*1), Y1; VMOVDQU Y1, 448(R11)
	VMOVDQA Y3, Y2; VPGATHERDD Y2, 60(R8)(Y7*1), Y1; VMOVDQU Y1, 480(R11)

	VZEROUPPER
	RET
